<h2>Riders Club FAQs</h2>

<b>Eligibility</b><br />
<ol>
	<li>What, if any, requirements are there to qualify for the service?<div class="answer"> Riders must be 55 years of age or older, or have a medical condition which keeps them from driving.</div></li>
	<li>Is any evaluation needed prior to using the service?<div class="answer"> All applicants, drivers and riders, undergo a background check.</div></li>
	<li>Are rides provided for wheelchair users or other persons with disabilities?<div class="answer"> We cannot accommodate wheelchairs but people with walkers and canes are able to use our service.</div></li>
	<li>If needed, can a family member serve as an escort?<div class="answer"> A caregiver can ride with a member at no additional cost.</div></li>
</ol>
<b>Affordability</b><br />
<ol>
	<li>What is the cost of the service?<div class="answer"> There is an annual fee and a prepaid mileage fee for each ride/destination.</div></li>
    <li>Are discounts available?<div class="answer"> The senior discount is applied to all rides.</div></li>
	<li>How are costs calculated?<div class="answer"> Each Club establishes their pricing structure.  Contact your local Club for details.</div></li>
	<li>Is my income a factor for using this service?<div class="answer"> We do not currently use income as a factor for eligibility.</div></li>
	<li>Can an account be set up in advance with the service?<div class="answer"> We require prepayment of services through the use of personal accounts. To prevent an interruption in your service, we will contact you when your account reaches the threshold you set (minimum $10). A minimum recharge of $40 is needed to keep your account active.</div></li>
</ol>
<b>Accessibility</b>
<ol>
	<li>What is the service area?<div class="answer"> Each Club identifies its service area.  We serve the Cedar Rapids metropolitan area, including Marion and Hiawatha. Boundaries: North:Tower Terrace Road, East: Highway 13, South: US 30, West: Stoney Point Rd.</div></li>
	<li>What times does the service operate?<div class="answer"> Rides are available 24 hours a day, 7 days a week.	Our office hours are 9:00 a.m. to 3:00 p.m., M-F.  We do not schedule rides outside office hours.</div></li>
	<li>Are door-through-door, door-to-door, or curb-to-curb services provided?<div class="answer"> We offer door-to-door services at RCA. This allows us to access any public space.</div></li>
	<li>Is a reservation needed, and how far in advance?<div class="answer"> We ask that you let us know no later than noon the business day before you need a ride, the sooner the better.</div></li>
	<li>Are rides provided in the evenings, on weekends, or on holidays?<div class="answer"> Yes! We are available when you need to be there.</div></li>
	<li>Are rides provided to social as well as medical or shopping appointments?<div class="answer"> Yes! We are available to take you where you want to go.</div></li>
	<li>Will the driver provide assistance with packages and other carry on items?<div class="answer"> Our drivers are encouraged to help you however they can, within their abilities.</div></li>
	<li>Are vehicles wheelchair accessible?<div class="answer"> We do not accommodate wheelchairs.</div></li>
	<li>If other passengers will be riding at the same time, what is the maximum length of time of the ride while others are being picked up or dropped off?<div class="answer"> On rare occasions, we will ask you in advance if you can share your ride. We will do this in such a way to minimize impact on your time. Usually, we will provide your service without other riders.</div></li>
	<li>I may need the use of a truck to pick up lumber. Can you use mine?<div class="answer"> Not at this time due to insurance issues.</div></li>
</ol>
<b>Other questions</b>
<ol>
	<li>Can you wait while I am inside?<div class="answer"> Your driver may have other rides scheduled after yours. A driver may wait up to 15 minutes at our convenience.</div></li>
    <li>Can my pet travel with me?<div class="answer"> Contact the office early so we can make sure a driver is available who will transport a secured pet.</div></li>
</ol>

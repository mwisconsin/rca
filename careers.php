We are always looking for people with a passion for caring about seniors. Volunteers are the backbone of our operation, and there are many ways to contribute. A limited number of paying position are filled from among those who have shown proficiency as volunteers.<br>
<br>
<b>Volunteer Cordinators</b> - the volunteer coordinator is the connecting link between riders and drivers.<br>
<br>
Responsibilities include:<br>
<ol>
	<li>Answering phones</li>
    <li>Scheduling rides with riders</li>
    <li>Contacting drivers to confirm availability for next day rides</li>
    <li>Help maintain stability of driver pool</li>
    <li>Other duties as assigned</li>
</ol>
<br>
Time required: up to 30 hours per week (partial roles available in 3 hour blocks)<br>
<br>
<b>Bookkeeper</b> - Fiscal responsibility is imperative for every non-profit organization.  The bookkeeper insures that funds are allocated properly and that resources are appropriately tracked.<br>
<br>
Responsibilities include:
<ol>
	<li>Helping maintain the financial stability of the organization</li>
    <li>Identify money receieved and making journal entries</li>
    <li>Identifying expenses and making journal entries</li>
    <li>preparing checks for distribution</li>
</ol>
<br>
Time required: up to 3 hours per week. Location negotiable<br>
<br>
<b>Volunteer Drivers</b> - Volunteer drivers make up the core of how we serve the community.  Based on each driver’s defined availability during a week, we contact drivers one day in advance to determine if they can drive.  Once a driver has fulfilled their preset time offering for the period (minimum of 4 hours per month), the system hides them from visibility until they enter a new period of time. <br>
<br>
<b>Market Developer</b> - – RCA must always be focused on the best ways to provide information to those who are interested in service.  Not only do we want to explain our service and how we serve the community, we also want to refer riders to the proper service when we cannot best meet their needs.  The Market Developer must identify how to present RCA efficiently and effectively.  Web development / deployment skills are required.<br>
